/**
 * 1秒のミリ秒数
 */
export const OneSecondMs = 1000;

/**
 * 1分のミリ秒数
 */
export const OneMinuteMs = OneSecondMs * 60;

/**
 * 1時間のミリ秒数
 */
export const OneHourMs = OneMinuteMs * 60;

/**
 * 1日のミリ秒数
 */
export const OneDayMs = OneHourMs * 24;

/**
 * 1週間のミリ秒数
 */
export const OneWeekMs = OneDayMs * 7;

/**
 * 1ヶ月のミリ秒数
 */
export const OneMonthMs = OneDayMs * 30;

/**
 * 1年のミリ秒数
 */
export const OneYearMs = OneDayMs * 365;
